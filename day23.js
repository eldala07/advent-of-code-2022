const padInput = (input, nbOfRounds) => {
	const paddedInput = input.map(row => Array.from({length: nbOfRounds}).map(() => ".").concat(row).concat(Array.from({length: nbOfRounds}).map(() => ".")));
	for (let i = 0; i < nbOfRounds; i++) {
		paddedInput.unshift(Array.from({length: paddedInput[0].length}).map(() => "."));
		paddedInput.push(Array.from({length: paddedInput[0].length}).map(() => "."));
	}
	return paddedInput;
}

const partOne = (rawInput, nbRounds) => {
	const input = padInput(rawInput, nbRounds+1);
	for (let i = 0; i < nbRounds; i++) {
		// console.log('File: compute.js, Line 145 -------------------: Round ', i+1);
		const directionsOrder = rotateDirections(["N", "S", "W", "E"], i);
		const movingElves = considerPositions(input, directionsOrder);
		movingElves.forEach(elf => {
			const values = Object.values(elf)[0];
			if (input?.[values.y]?.[values.x]) {
				input[values.y][values.x] = ".";
				let newY = values.y;
				let newX = values.x;
				switch (values.direction) {
					case "N":
						newY--;
						break;
					case "E":
						newX++;
						break;
					case "S":
						newY++;
						break;
					case "W":
						newX--;
						break;
				}
				if (input?.[newY]?.[newX]) {
					input[newY][newX] = "#";
				}
			}
		});
	}
	// input.forEach(row => console.log(row.join("")));
	return countNumberOfEmptyGroundTiles(input);
};


